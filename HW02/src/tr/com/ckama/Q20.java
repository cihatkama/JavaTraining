package tr.com.ckama;

public class Q20 {

	public static void main(String[] args) {
		float piFloat = 3.1415f;
		double piDouble = 3.1415;
		byte piByte = (byte) 3.14;
		short piShort = (short) 3.1415;

		System.out.println("float value : " + piFloat);
		System.out.println("double value : " + piDouble);
		System.out.println("byte value : " + piByte);
		System.out.println("short value : " + piShort);
	}

}
